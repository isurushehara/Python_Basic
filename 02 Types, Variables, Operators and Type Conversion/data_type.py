# Data Types in Python
print("Data Types in Python")

print("Integer:", 42)
print("Float:", 3.14)
print("String:", "Hi")
print('String with single quotes:', 'Hi')
print("Boolean:", True)
print("Complex Number:", 1 + 2j)

#Escape Characters
print("\nEscape Characters")
print("One: 1 \nTwo: 2 \nThree: 3")
print("One\t: 1")
print("Two\t: 2")
print("Three\t: 3")
print("Backslash: \\ -> (eg: C:\\Users\\Name)")
print("Double Quote: \" -> (eg: \"Hello\")")

