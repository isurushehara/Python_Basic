## Variables 
# No need to add type of the data, just assign the value with a proper variable name
# variable_name = value
# Example of variable assignment in Python
print("Variables in Python:")
integerNumber = 42 # Integer type
print("Integer:", integerNumber)
floatNumber = 3.14 # Float type
print("Float:", floatNumber)
stringName = "Isuru" # String type
print("String:", stringName)
booleanValue = True # Boolean type
print("Boolean:", booleanValue)

# Check the type of the variable
print("\nType of variables:")   
print("Type of integerNumber:", type(integerNumber))  # Output: <class 'int'>
print("Type of floatNumber:", type(floatNumber))  # Output: <class 'float'>
print("Type of stringName:", type(stringName))  # Output: <class 'str'>
print("Type of booleanValue:", type(booleanValue))  # Output: <class 'bool'>)
# print with ',' separator
print("\nPrint with ',' separator:")    
print("Hello", "World", "from", "Python")  # Output: Hello World from Python

# Arithmetic Operators
print("\nArithmetic Operators:")     
# Addition
print("Addition:", 5 + 3)  # Output: 8
# Subtraction
print("Subtraction:", 10 - 4)  # Output: 6
# Multiplication
print("Multiplication:", 2 * 3)  # Output: 6
# Division
print("Division:", 8 / 2)  # Output: 4.0
# Modulus
print("Modulus:", 10 % 3)  # Output: 1
# Exponentiation
print("Exponentiation:", 2 ** 3)  # Output: 2x2x2 = 8
# Floor Division
print("Floor Division:", 7 // 2)  # Output: 3

# Comparison Operators
print("\nComparison Operators:") 
# Equal to
print("Equal to:", 5 == 5)  # Output: True
# Not equal to
print("Not equal to:", 5 != 3)  # Output: True
# Greater than
print("Greater than:", 5 > 3)  # Output: True
# Less than
print("Less than:", 5 < 10)  # Output: True
# Greater than or equal to
print("Greater than or equal to:", 5 >= 5)  # Output: True
# Less than or equal to 
print("Less than or equal to:", 5 <= 10)  # Output: True

# Logical Operators
print("\nLogical Operators:")
# Logical AND
print("Logical AND:", True and False)  # Output: False
# Logical OR
print("Logical OR:", True or False)  # Output: True
# Logical NOT
print("Logical NOT:", not True)  # Output: False
# Logical XOR (exclusive or)
print("Logical XOR:", True ^ False)  # Output: True
# Logical shift
print("Logical Shift:", 5 >> 1)  # Output: 2 (right shift)

# Bitwise Operators
print("\nBitwise Operators:")
# Bitwise AND
print("Bitwise AND:", 5 & 3)  # Output: 1
# Bitwise OR
print("Bitwise OR:", 5 | 3)  # Output: 7
# Bitwise XOR
print("Bitwise XOR:", 5 ^ 3)  # Output: 6
# Bitwise NOT
print("Bitwise NOT:", ~5)  # Output: -6
# Bitwise Left Shift
print("Bitwise Left Shift:", 5 << 1)  # Output: 10
# Bitwise Right Shift
print("Bitwise Right Shift:", 5 >> 1)  # Output: 2

# Assignment Operators
print("\nAssignment Operators:")
# Simple assignment
x = 10
print("Simple Assignment:", x)  # Output: 10
# Add and assign
x += 5
print("Add and Assign:", x)  # Output: 15
a, b = 5, "Isuru"
print("Multiple Assignment:", a, b)  # Output: 5 Isuru

# Rules in using operators
print("\nRules in using operators:")    
# String concatenation
print("String Concatenation:", "Hello" + " " + "World")  # Output: Hello World
# String repetition
print("String Repetition:", "Hello " * 3)  # Output: Hello Hello Hello


